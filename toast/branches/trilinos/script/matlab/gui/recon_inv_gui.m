function prm = prm_setdefaults()
prm = [];
prm.basis.bdim = [1 1 1];
prm.basis.usefinegrid = false;
prm.method = 'LM';
prm.tol = 1e-8;
prm.regul.method = 'None';


function setguifromprm(handles,prm)
if isfield(prm,'basis')
    if isfield(prm.basis,'bdim')
        h = [handles.edit1,handles.edit2,handles.edit3];
        for i = 1:length(prm.basis.bdim)
            set(h(i),'String',num2str(prm.basis.bdim(i)));
        end
    end
end
if isfield(prm,'method')
    mthd = {'PCG','LM'};
    idx = find(strcmpi(mthd,prm.method));
    set(handles.popupmenu1,'Value',idx);
end
if isfield(prm,'tol')
    set(handles.edit7,'String',num2str(prm.tol));
end


